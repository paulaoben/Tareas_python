"""
Operaciones matemáticas con Python

Suma +
Resta -
Multiplicación *
División /
División entera //
Módulo (resto) %
Potenciación **

"""

num1= 150
num2= 200
num3= 1035
num4= 100
resultado_suma = num1+num2
resultado_resta= num2-num1
resultado_multiplica= num1*num2
resultado_division= num3 / num4
resultado_division_exacta= num3 // num4
resultado_division_residuo= num3 % num4

print("El resultado de la suma es :" , resultado_suma)
print("El resultado de la resta es: " , resultado_resta)
print("El resultado de la multiplicación es: " , resultado_multiplica)
print("El resultado de la división es: " , resultado_division)
print("El resultado de la división exacta es: " , resultado_division_exacta)
print("El resultado de la división residuo es: ", resultado_division_residuo)
print("El resultado de la potenciación es: " , 5**3)


